public class Conditionals {
	public static void main(String[] args) {
		int number = 15;

		if (number % 2 == 0) {
			System.out.println(number + " is a multiple of 2.");
		} 
		if (number % 3 == 0) {
			System.out.println(number + " is a multiple of 3.");
		}
		if (number % 5 == 0) {
			System.out.println(number + " is a multiple of 5.");
		}
	}
}