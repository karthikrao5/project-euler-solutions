public class Loop {
    public static void main(String[] args) {

        System.out.println("For loop: ");
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }

        System.out.println("\nWhile loop: ");
        int i = 0;
        while (i < 10) {
            System.out.print(i + " ");
            i++;
        }
    }
}